    public Results(String sql){
        this.sql=sql;
        initComponents();
        
        DefaultTableModel model=(DefaultTableModel)table_results.getModel();
        
        try{
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/resume","root","5431");
            PreparedStatement pst=con.prepareStatement(sql);
            ResultSet rs=pst.executeQuery(sql);
            
            while(rs.next()){
                String usn = rs.getString("usn");
                String f_name = rs.getString("first_name");
                String l_name = rs.getString("last_name");
               
                model.addRow(new Object[]{usn,f_name,l_name});     
            }
        }
        catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Database connectivity error");
        }
    }

